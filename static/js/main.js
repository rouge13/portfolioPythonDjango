function hidePhoneNumber() {
    var x = document.getElementById("phoneNumber");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}

function hideLocation() {
    var x = document.getElementById("location");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}